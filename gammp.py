T=187.63408999999996 #—Å—Ä–µ–¥–Ω–µ–µ –≤—Ä–µ–º—è –Ω–∞—Ä–∞–±–æ—Ç–∫–∏ –¥–æ –æ—Ç–∫–∞–∑–∞
